package course25;

public class 復習 {

	public static void main(String[] args) {
		int a = 30;
		int b = 50;
		huku();
		hukuCalc();
		hukuTasu(a, b);
	}

	static void huku() {
		System.out.println("復習問題です");
	}

	static void hukuCalc() {
		System.out.println(1 + 1);
	}

	static void hukuTasu(int x, int y) {
		System.out.println(x + y);

	}

}
